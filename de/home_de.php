<h2>Ein paar Worte über die Konferenz der PhD Studenten </h2>

<p>
Seit 1993 organisiert die <strong>STEP'UP Doktorandenschule </strong> 
den alljährlich stattfindenden Kongree der Doktoranden, den 
<strong>“Congrès Des Doctorants” (CDD)</strong>. Ziel ist es, die 
Forschungsergebnisse der Doktoranden vom Institut de Physique du Globe de Paris (IPGP), 
der Universität Diderot, der École Normale Supérieure (ENS) und der Universität Pierre 
et Marie Curie (UPMC) zu fördern. Auf diesem Kongress werden die Forschungsergebnisse 
von Doktoranden des ersten Jahres in Form von Poster-Präsentationen und von Doktoranden 
des dritten Jahres in Form von Vorträgen präsentiert - dies gilt auch für Doktoranden 
anderer europäischer Universitäten. Die Konferernz wird traditionsgemäß von Doktoranden 
des ersten Jahres organisiert, die beiden Komponenten der Doktroandenschule angehören: 
Umweltwissenschaften und Physik des Universums. Dies vergrößert deutlich den Themenbereich 
und reflektiert die Interdisziplinarität der Doktorandenschule STEP'UP.  
<strong> Die 2020 Edition der Konferenz wird ausnahmsweise am IPGP am 29. und 30. Oktober 2020 </strong>stattfinden.</p>



<h3>
Überblick über das Programm
</h3>

<p>
Die Erste Tag wird für Möglichkeiten in der Arbreitswelt gewidmet sei. 
Hierzu werden Experten aus Industrie und Wissenschaft 
Vorträge präsentieren, Treffen abhalten und Trainings, die die Fähigkeiten der 
Studenten untersützen werden. 
Am zweiten Tag wird der Vormittag den Postern der Studenten gewidmet.

Am Nachmittag des zweiten Tages wird der Fokus auf dem Thema 
<strong>“Exoplanet”</strong> liegen. Führende Wissenschaftler werden dafür 
Präsentationen halten. Am Ende der Konferenz wird es einen Empfang geben, 
der von der Hekla association oganisiert wird. All Teilnehmer sind in dessen 
Rahmen herzlich eingeladen, sich bei Speisen und Getränken auszutauschen und 
neue Kontakte zu knüpfen. 

