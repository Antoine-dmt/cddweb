<h3 class="major">Se rendre sur place</h3>

<h4>Depuis l’aéroport Roissy-Charles de Gaulle :</h4>
<ul>
	<li>Prendre la ligne de RER B direction Saint-Remy les Chevreuses.</li>
	<li>Descendre à l’arrêt Saint-Michel et prendre la ligne 10.</li>
	<li>Sortir à l’arrêt Jussieu.</li>
</ul>
<div>Coût: 10€ </div>
<div>Durée: 1h</div>

<h4>Depuis l'aéroport Orly : </h4>

<ul>
        <li>Prendre un OrlyBus jusqu'à la station Denfert-Rochereau</li>
        <li>Prendre la ligne 6 direction Nation (un nouveau ticket sera nécessaire)</li>
        <li>Changement à Place d’Italie et prendre la ligne 7 direction La Courneuve</li>
		<li>Sortir à l'arrêt Jussieu</li>
</ul>
<div>Coût:  10€20 (8€30 OrlyBus + 1€90 Métro à Paris) </div>
<div>Durée: 1h</div>

<h4>Depuis Gare du Nord :</h4>

<ul>
	<li>Prendre la ligne de RER B direction Saint-Remy les Chevreuses.</li>
	<li>Descendre à l’arrêt Saint-Michel et prendre la ligne 10.</li>
	<li>Sortir à l’arrêt Jussieu.</li>
</ul>
<div>Coût: 1€90 </div>
<div>Durée: 30min</div>

<h4>Depuis Gare de Lyon :</h4>

<div>Soit : 20 minutes à pied. </div>
<div>Soit : Ligne de bus 63 direction Porte de la muette, descendre à l’arrêt Cuvier.</div>

