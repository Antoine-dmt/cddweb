<h2>
	Le congrès des doctorants en quelques mots
</h2>
<p>
	Depuis 1993, <strong>l'école doctorale STEP'UP organise le "Congrès Des Doctorants" (CDD)</strong>, 
	une conférence annuelle pour promouvoir la recherche des doctorants de l'Institut de Physique 
	du Globe de Paris (IPGP), de l'Université Paris Diderot (UPD), de l'École Normale Supérieure (ENS) 
	et de l'Université Pierre et Marie Curie (UPMC). Le congrès est organisé par les doctorants de 
	1ère année des deux composantes de l'école doctorale (Sciences de l'Environnement et Physique de 
	l'Univers) et comprendra des exposés de vulgarisation de chercheurs ainsi que des posters et des 
	présentations de doctorants de STEP'UP et d'autres étudiants invités. L'objectif de cette conférence 
	est d'établir des liens entre étudiants, chercheurs et professionnels dans les domaines des géosciences 
	et de la physique de l'univers. <strong>L'édition 2022 se tiendra à l'IPGP entre le 13 et le 15 avril.</strong>

</p>

