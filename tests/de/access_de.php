<h3 class="major">Wegbeschreibung:</h3>

<h4>Start von Roissy- Charles de Gaulle Flughafen</h4>

<ul>
        <li>Nehmen Sie die RER B in Richung Saint-Remy les Chevreuses</li>
        <li>Wechseln Sie an der Station Saint-Michel in die U-Bahn Linie 10</li>
        <li>Steigen Sie an der Station Jussieu aus</li>
</ul>
<p>Kosten: 10€</br>
Dauer: 1 Stunde </p>

<h4>Start vom Flughafen Orly</h4>

        <li>Nehmen Sie den OrlyBus bis zum Ende der Linie (Station Denfert-Rochereau)</li>
        <li>Nehmen Sie anschließend Linie 6 in Richtung Nation (Sie müssen ein neues Ticket lösen)</li>
        <li>Wechseln Sie an der Station Place d'Italie und steigen Sie in Linie 7 in Richtung La Courneuve ein</li>
        <li>Steigen Sie an der Station Jussieu aus</li>
<p>Kosten:  10€20 (8€30 OrlyBus + 1€90 Métro in Paris)</p>
<p>Dauer: 1 Stunde</p>
<h4>Start vom Norbahnhof Paris (Gare du Nord)</h4>
        <li>Nehmen Sie die RER B in Richung Sain-Remy les Chevreuses</li>
        <li>Wechseln Sie an der Station Saint-Michel und steigen Sie in Linie 10 ein</li>
        <li>Steigen Sie an der Station Jussieu aus</li>

<p>Kosten: 1€90 </br>
Dauer: 30min</p>

<h4>Start vom Bahnhof Gare de Lyon:</h4>

<p>Entweder: 20 minutes Fußmarsch. </br>
Oder : Bus Linie 63 in Richtung Porte de la Muette, Steigen Sie an der Haltestelle Cuvier aus.</p>

