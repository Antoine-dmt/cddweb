<h3 class="major">Deadlines</h3>

<p>Der Congrès des Doctorants (CDD) wird vom <strong>23. bis 27. März 2020</strong> stattfinden. Die Registirerung ist eröffnet und Abstracts sollten vor Ablauf der Deadline <strong>01/02/2020</strong> eingereich werden. </p>

<p>Jeder PhD Student der STEP'UP Doktorandenschule <strong> muss am CDD teilnehmen </strong>. Falls aus belegbaren Gründen nicht am CDD teilgenommen werden kann, muss dennoch ein Abstract bis zur angegebenen Deadline eingereicht werden (mit der Notiz nicht am Kongress teilnehmen zu können), und das zu präsentierede Poster bis zum 22. März (12:00) im Büro der Kongress-Organisation abgegeben werden. Bitte senden Sie so früh wie möglich eine Email an das Logistik-Team, um mit Ihrer Abwesenheit planen zu können. </p>

<p>Vergessen Sie nicht die <strong>Logos</strong>  der Doktorandenschule und Ihrem Labor entweder auf dem Poster oder in die Präsentation einzufügen</p>

<h3 class="major">Abstracts</h3>

<p>Sprache : <strong>nur Englisch zulässig</strong></br>
Format : Titel, Autoren, Affiliation (Institut und Labor), Abstract (<strong>200 - 360 Worte.</strong>), Schlüsselworte, keine Abbildungen. </p>

<p>Um sich zu <strong>registrieren</strong> müssen Sie zuerst das <strong>Program</strong> oder <strong>Web</strong> Team kontaktieren, sodass diese Ihre Email-Adresse für de Registrierung autorisieren können. Wenn Sie registriert sind und angemeldet haben können Sie im Abstract-Bereich bei <strong>Einreichen</strong> Ihren Abstract einreichen. Falls erforderlich müssen Gleichungen und Sonderzeichen im LaTeX-Syntax bereitgestellt werden und es können zudem nur PDF Formate unterstützt werden. </p>


<h3 class="major">Vorträge</h3>

<p>
	Sprache : <strong>nur Englisch zulässig</strong></br>
	Formats : Wenn Sie Ihren Abstract einreichen, können sie aus diesen Optionen wählen:  
	<ul>
		<li><strong>10 Minuten</strong> Länge der Präsentation, unterstützt mit Folien, gefolgt von 5 Minuten für Fragen.</li>
		<li><strong>15 Minuten</strong> Länge der Präsentation, unterstützt mit Folien, gefolgt von 5 Minuten für Fragen für die "popularisation sessions".</li>
	</ul>
</p>

<p>Popularised talk: Es sollte ein Vortrag sein, der thematisch für Doktoranden beider Komponenten der Doktorandenschule (Terre environnement oder Physique de l'Univers)interessant ist. Der Votrag sollte für ein gebildetes wissenschaftliches Publikum zugänglich sein. Dafür werden Ihnen 5 Minuten mehr Zeit eingeräumt um gegebenenfalls Grundlagen zu erläutern. 

<p>Logistik: Sie müssen mindestens 20 Minuten vor Start der Session vor Ort sein, um Ihren Vortrag zu überspielen und ihre Folien zu verifizieren. Zusätzlich muss zum ursprünglichen Format (z.B. Powerpoint) Ihr Vortrag als <strong>PDF format</strong> abgespeichert werden. Bitte benutzen Sie hierzu einen USB-Stick der mit Mac Rechnern kompatibel ist. Falls nöting wird eine Internet-Verbindung sowie ein Laserpointer bereitgestellt.  </p>


<h3 class="major">Poster</h3>

<p>Sprache : <strong>nur Englisch zulässig</strong></br>
Format : A0 : 1189 x 840 mm Portraitformat gewünscht</p>

<p>Bitte<strong> seien Sie am Tag Ihrer Session zwischen 10:00 und 11:00 am IPGP</strong> um Ihr Poster aufzuhängen. Die Poster werden in Ihrem jeweiligen Labor gedruckt.</p> 

<p>
	<strong>Ratschläge zur Erstellung eines Wissenschaftlichen Posters: </strong></br>
	<ul>	
    	<li>Falls Sie Adobe Illustrator benutzen, vektorisieren Sie den kompletten Text (alles auswählen, Text Menü → Vektorisieren (bevor Sie es als PDF abspeichern) </li>
		<li>Falls Ihre Dateigröße 75 Mb übersteigt ist es möglich nahezu ohne Qualitätsverlust mit Acrobat Professional die Dateigöße zu reduzieren (Dokument → Filegröße reduzieren).</li>
		<li>Bitte wählen Sie einen Schrifttypen ohne Serifen (z.B. Helvetica, Arial, etc.), um eine bessere Lesbarkeit zu gewährleisten.</li>
    	<li>Das Poster muss aus einer Entfernung von 2 Metern deutlich lesbar sein, verwenden Sie dazu nur Schriftgrößen über 18 pt. </li>
	</ul>
</p>