<h3 class="major">How to get there:</h3>

<h4>From Roissy-Charles de Gaulle airport:</h4>

<ul>
        <li>Take RER B direction Saint-Remy les Chevreuses.</li>
        <li>Change at Saint-Michel and take line 10.</li>
        <li>Get off at Jussieu stop.</li>
</ul>
<p>Cost: 10€ </br>
Duration: 1h </p>

<h4>From Orly airport :</h4>

        <li>Take the OrlyBus until the end of the line (Denfert-Rochereau)</li>
        <li>Take line 6 direction Nation (you will need a new ticket)</li>
        <li>Change at Place d’Italie and take line 7 direction La Courneuve</li>
        <li>Get off at Jussieu stop.</li>
<p>Cost:  10€20 (8€30 OrlyBus + 1€90 Métro in Paris)</p>
<p>Duration: 1h</p>
<h4>From Gare du Nord:</h4>
        <li>Take RER B direction Saint-Remy les Chevreuses.</li>
        <li>Change at Saint-Michel and take line 10.</li>
        <li>Get off at Jussieu stop.</li>

<p>Cost: 1€90 </br>
Duration: 30min</p>

<h4>From Gare de Lyon:</h4>

<p>Either : 20 minutes walk. </br>
Or : Bus line 63 direction Porte de la Muette, get off at Cuvier stop.</p>

