<h2 class="class_test">
	Le congrès des doctorants en quelques mots
</h2>
<p>
	Depuis 1993, <strong>l'école doctorale STEP'UP organise le « Congrès Des Doctorants » (CDD)</strong>, 
	une conférence annuelle pour promouvoir la recherche des étudiants en doctorat à l'Institut de Physique 
	du Globe de Paris (IPGP), à l'Université Paris Diderot (UPD), à L'École Normale Supérieure (ENS) et à 
	l’Université Pierre et Marie Curie (UPMC). Ce congrès est axé autour de présentations de posters faits par 
	les étudiants en 1ère et 2ème année de doctorat et des présentations orales réalisées par les étudiants 
	en 3ème année et des étudiants invités venant d'autres universités européennes. Ce congrès a pour but d’établir des liens entre les étudiants mais aussi 
	de créer des liens avec des chercheurs et des professionnels dans les domaines des géosciences et de la 
	physique de l’univers. <strong>L'édition 2020 se tiendra à l'IPGP entre le 23 et le 27 mars.</strong>
</p>

<h3>Programme de la semaine</h3>
<p> 
	La semaine sera organisée autour de différentes sessions centrées autour de nombreux thèmes de recherche 
	présents au sein de l’école doctorale. En plus des présentations des étudiants, 
	une journée sera consacrée à l'emploi, à travers une matinée de rencontres et de 
	présentations de professionnels de l'industrie et académiques, et une après-midi 
	de formation pour apprendre à promouvoir ses compétences. La dernière demi-journée 
	sera consacrée à des présentations de la part de chercheurs reconnus sur le sujet 
	d'ouverture <strong> « Exoplanète » </strong>. Le congrès sera clôturé par un pot organisé par l’association 
	Hekla, permettant aux participants de se retrouver autour d'un repas ou d'un verre.
</p>

<h3>Comment participer ?</h3>

<p>
	Les étudiants d'autres universités européennes souhaitant participer à la conférence sont priés de soumettre un abstract. Si leur abstract est sélectionné, l'intégralité de leurs frais de transport et d'hébergement sera couverte. La <strong>date limite pour soumettre un abstract est le 2 février 2020</strong>.
</p>

