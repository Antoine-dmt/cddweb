<h2 class="major">Informations</h2>

<h3 class="major">Dates limites</h3>

<p>
	Le Congrès des Doctorants (CDD) se déroulera du <strong>23 au 27 Mars 2020</strong>. Les inscriptions sont ouvertes et tous les résumés doivent être soumis avant le <strong>2 février 2020 </strong>.
</p>

<p>
	Tous les doctorants de l'ED STEP'UP <strong>doivent participer au CDD</strong>. Si vous n'êtes pas disponible vous devez soumettre un résumé en notifiant votre absence et déposer votre poster avant le <strong> [...] mars à midi </strong> à l'IPGP dans le bureau de Prisca Rasolofomanana (salle P32, 3ème étage du côté de l'enseignement). Veuillez aussi envoyer un e-mail à l'équipe de logistique du CDD pour notifier votre absence le plus tôt possible.
</p>

<p>
	N'oubliez pas d'inclure les <strong>logos</strong> de l'école doctorale et de votre laboratoire sur votre poster ou votre présentation.
</p> 

<h3 class="major">Résumés</h3>

<p>Langue : <strong>anglais uniquement</strong></br>

Format : Titre, auteurs, affiliations (institut et laboratoire), résumé (<strong>200 - 360 mots.</strong>), mots clef ou thématiques, pas d’illustrations. </p>

<p>Pour vous <strong>inscrire</strong>, vous devez d'abord contacter l'équipe <strong>web</strong> ou <strong>program</strong> afin qu'ils autorisent votre adresse e-mail à servir pour l'inscription. Vous pourrez alors utiliser le lien en haut à droite du site pour cela. Une fois connecté, vous pourrez <b>soumettre</b> votre résumé. Si besoin, les équations et caractères spéciaux doivent être en syntaxe LaTeX. Seul le format PDF est accepté.</p>

<h3 class="major">Présentations orales</h3>

<p>
	Langue : <strong>anglais uniquement</strong></br>
	Format : Lorsque vous soumettez votre résumé, vous avez le choix entre :
	<ul>
		
		<li><strong>10 minutes</strong> de présentation avec diapositives suivie de 5 minutes de questions. </li>
		<li><strong>15 minutes</strong> de présentation avec diapositives suivie de 5 minutes de questions, pour les sessions de vulgarisations. </li>
	</ul>
</p>

<p><strong>Oral de vulgarisation :</strong> L'oral est destiné à tous les étudiants de l'école doctorale, incluant ceux de l'autre composante (Terre environnement ou Physique de l'Univers). Il doit donc être accessible pour un public scientifique éclairé. Vous aurez 5 minutes additionnelles pour expliquer ce que vous faites depuis les principes fondamentaux.</p>

<p><strong>Logistique :</strong> Vous devez vous présenter au moins 20 minutes avant le début de votre session afin de copier et vérifier vos diapositives sur l’ordinateur utilisé pour les présentations. En plus du format initial (PowerPoint, LibreOffice Impress, Beamer, etc.), vos diapositives devront être sauvegardées au <strong>format PDF</strong> sur une clef USB compatible avec un ordinateur Mac. Si nécessaire, une connexion internet sera disponible. Un pointeur laser sera mis à votre disposition.</p>

<h3 class="major">Posters</h3>

<p>
	Langue : <strong>anglais uniquement</strong></br>
	Format : A0 : 1189 x 840 mm préférentiellement en portrait
</p>

<p>
	Merci d'<strong>être présent à l'IPGP entre 10 et 11h</strong> le jour de votre session pour accrocher votre poster. Les posters sont à imprimer dans vos laboratoires respectifs.
</p>

<h4>Conseils pour réaliser un poster scientifique :</h4>

<p>
	<ul>
		
	    <li>Sous Illustrator, pensez à vectoriser le texte (sélectionner l'ensemble du texte, puis Texte → Vectoriser) avant d'enregistrer une copie en PDF.</li>
	    <li>Si le poster est trop "lourd" (taille supérieur à 75 Mo), sa taille peut être réduite par Acrobat Professionnel, sans perdre en qualité (Documents → Réduire la taille du fichier).</li>
	    <li>Préférez si possible une seule police de caractère sans-serif (par exemple Helvetica, Arial) et ses déclinaisons (par exemple gras, italique) pour une lecture plus agréable.</li>
	    <li>Le poster doit être lisible à une distance de 2m (<strong>aucun texte en-dessous de 18 pts</strong>).</li>
	</ul>
</p>
